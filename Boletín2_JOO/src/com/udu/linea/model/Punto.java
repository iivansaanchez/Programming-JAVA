package com.udu.linea.model;

public class Punto {
	
	private double x;
	private double y;
	
	public Punto() {
		super();
		this.x=0.0;
		this.y=0.0;
	}


	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	
	
	
	
	

}
